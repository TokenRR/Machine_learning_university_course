#b. Середній дохід від продажу б/в транспортних засобів в місяці М,
#де М – це порядковий номер у списку підгрупи за абеткою.
#M=15 отже MARCH
print('\n#b. Середній дохід від продажу б/в транспортних засобів в місяці March,')
