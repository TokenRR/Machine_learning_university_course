#5. Привести поля, що відповідають обсягам продаж, до числового вигляду(показати, що це виконано).
print('\n#5. Привести поля, що відповідають обсягам продаж, до числового вигляду(показати, що це виконано).')
df['Total_Sales_New'] = df['Total Sales New'].apply(lambda x :x.replace('$','')).astype(int)
df['Total_Sales_Used'] = df['Total Sales Used'].apply(lambda x :x.replace('$','')).astype(int)
print(df.info())


#6. Ввести нові поля:
#a. Сумарний обсяг продаж автомобілів (нових та б/в) у кожний
#період;
#b. Сумарний дохід від продажу автомобілів (нових та б/в) у кожний
#період;
#c. Різницю в обсязі продаж нових та б/в автомобілів у кожній
#період.
print('\n#6. Ввести нові поля: ')
df['Total_used+new'] = df['New'] + df['Used'] #a
df['Total_sales_used+new'] = df['Total_Sales_New'] + df['Total_Sales_Used'] #b
df['Difference_used-new'] = df['Used'] - df['New'] #c
print(df[['Total_used+new','Total_sales_used+new','Difference_used-new']].head())


#7. Змінити порядок розташування полів таким чином: Рік, Місяць,
#Сумарний дохід, Дохід від продажу нових автомобілів, Дохід від
#продажу б/в автомобілів, Сумарний обсяг продаж, Обсяг продаж нових
#автомобілів, Обсяг продаж б/в автомобілів, Різниця між обсягами
#продаж нових та б/в автомобілів.
print('\n#7. Змінити порядок розташування полів первним чином')
df_new= df[['Year','Month','Total_sales_used+new','Total_Sales_New','Total_Sales_Used','Total_used+new','New','Used','Difference_used-new']]
print(df_new.head())


#8. Визначити та вивести:
print('\n#8. Визначити та вивести:')
#a. Рік та місяць, у які нових автомобілів було продано менше за б/в;
print('\n#a. Рік та місяць, у які нових автомобілів було продано менше за б/в;')
print(df_new.query('New<Used')[['Year','Month']])
#b. Рік та місяць, коли сумарний дохід був мінімальн
print('\n#b. Рік та місяць, коли сумарний дохід був мінімальни')
print(df_new[df_new['Total_sales_used+new']==df_new['Total_sales_used+new'].min()][['Year','Month']])
#c. Рік та місяць, коли було продано найбільше б/в авто
print('\n#c. Рік та місяць, коли було продано найбільше б/в авто')
print(df_new[df_new['Used']==df_new['Used'].max()][['Year','Month']])


#9. Визначити та вивести:
